#include "Calculator.h"
#include "Calculator2.h"
namespace Calc = Calculator;

int Calc::sum(int a, int b)
{
    return (a + b);
}

int Calc::substract(int a, int b)
{
    return (a - b);
}

int Calc::multiply(int a, int b)
{
    return (a * b);
}

double Calc::divide(int a, int b)
{
    return (a / b);
}
